	public class ListTableModel
	extends AbstractTableModel
	{
		public ListTableModel() {
			super();
		}

		public int getRowCount() {
			int retval;
			if( arrayOfISOLanguage != null ) {
				retval = arrayOfISOLanguage.length;
			}
			else {
				retval = 0;
			}
			return( retval );
		}

		public int getColumnCount() {
			return( NUM_COLS );
		}

		public String getColumnName( int column ) {
			String retval;
			switch( column ) {
				case COLID_ROW_HEADER:
					retval = "ISO Language";
					break;
				case COLID_OBJQUALIFIEDNAME:
					retval = "Qualified Name";
					break;
				case COLID_ID:
					retval = "Id";
					break;
				case COLID_ISOCODE:
					retval = "ISOCode";
					break;
				case COLID_BASELANGUAGECODE:
					retval = "BaseLanguageCode";
					break;
				case COLID_ISOCOUNTRYID:
					retval = "ISOCountryId";
					break;
				default:
					retval = null;
					break;
			}
			return( retval );
		}

		public Object getValueAt( int row, int column ) {
			final String S_ProcName = "getValueAt";
			if( ( row < 0 ) || ( column < -1 ) ) {
				return( null );
			}
			if( arrayOfISOLanguage == null ) {
				return( null );
			}
			int len = arrayOfISOLanguage.length;
			if( row >= len ) {
				return( null );
			}
			ICFBamISOLanguageObj obj = arrayOfISOLanguage[row];
			if( obj == null ) {
				return( null );
			}
			Object retval;
			switch( column ) {
				case COLID_ROW_HEADER:
					retval = obj;
					break;
				case COLID_OBJQUALIFIEDNAME:
					retval = obj.getObjQualifiedName();
					break;
				case COLID_ID:
					retval = new Short( obj.getRequiredId() );
					break;
				case COLID_ISOCODE:
					retval = obj.getRequiredISOCode();
					if( retval == null ) {
						retval = "";
					}
					break;
				case COLID_BASELANGUAGECODE:
					retval = obj.getRequiredBaseLanguageCode();
					if( retval == null ) {
						retval = "";
					}
					break;
				case COLID_ISOCOUNTRYID:
					retval = obj.getOptionalISOCountryId();
					break;
				default:
					retval = null;
					break;
			}
			return( retval );
		}

		public boolean isCellEditable( int row, int column ) {
			return( false );
		}
	}

